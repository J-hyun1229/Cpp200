#include <iostream>

using namespace std;

int main() {

	char c = 'C';
	int n = 100;
	double d = 3.14159;
	bool b = true;
	string word = "Hello World!";

	cout << "char: " << c << endl;
	cout << "int: " << n << endl;
	cout << "double: " << d << endl;
	cout << "bool: " << b << endl;
	cout << "string: " << word << endl;

	return 0;
}