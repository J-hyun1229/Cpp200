#include <iostream>

using namespace std;

int main() {
	char c1 = 'c';
	char c2 = 200;

	unsigned char c3 = 'c';
	unsigned char c4 = 200;

	printf("char c1 = %c, %d\n", c1, c1);
	printf("char c2 = %c, %d\n", c2, c2);
	printf("char c3 = %c, %d\n", c3, c3);
	printf("char c4 = %c, %d\n", c4, c4);

	return 0;

}